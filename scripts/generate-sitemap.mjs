/**
 * Regenerate public/sitemap.xml from known routes (keep in sync with src/config/site.ts).
 * Usage: node scripts/generate-sitemap.mjs
 */
import { writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const base = (process.env.PUBLIC_SITE_URL || 'https://www.solarhuntington.com').replace(/\/$/, '');
const lastmod = new Date().toISOString().slice(0, 10);

const guidesDir = join(__dirname, '..', 'src', 'content', 'guides');

const serviceSlugs = [
  'residential-solar-installation',
  'commercial-solar',
  'battery-storage',
  'solar-repair-maintenance',
  'free-solar-quote',
];
const areaSlugs = [
  'huntington',
  'huntington-station',
  'greenlawn',
  'northport',
  'cold-spring-harbor',
  'centerport',
];

const topLevelPages = [
  '/',
  '/about/',
  '/financing/',
  '/case-studies/',
  '/insights/',
  '/guides/',
  '/incentives/',
  '/warranty/',
  '/process/',
];

const insightSlugs = [
  'how-much-does-solar-cost-in-huntington-ny-2026',
  'solar-permits-town-of-huntington-2026-walkthrough',
  'tesla-powerwall-vs-enphase-iq-battery-2026',
];

// Daemon-shipped editorial guides under /guides/. Sourced via glob over
// src/content/guides/*.md so the sitemap always matches what the build
// produces — no more orphaned guides from out-of-band content drops.
// Previously hard-coded; switched 2026-06-26 after the
// when-should-you-replace-your-roof guide shipped to /guides/ but was
// absent here for 2 nights running.
const guideSlugs = readdirSync(guidesDir)
  .filter((f) => f.endsWith('.md'))
  .map((f) => f.replace(/\.md$/, ''))
  .sort();

// Cross-product service × area pages generated by tools/page-generator/.
// Add new (service, area) tuples here as pages are generated; the generator
// will also append them automatically in a future phase.
const crossProductPages = [
  ['battery-storage', 'huntington'],
  ['battery-storage', 'northport'],
  ['battery-storage', 'cold-spring-harbor'],
  ['battery-storage', 'huntington-station'],
  ['battery-storage', 'greenlawn'],
  ['battery-storage', 'centerport'],
  ['residential-solar-installation', 'huntington'],
  ['residential-solar-installation', 'northport'],
  ['residential-solar-installation', 'cold-spring-harbor'],
  ['residential-solar-installation', 'huntington-station'],
  ['residential-solar-installation', 'greenlawn'],
  ['residential-solar-installation', 'centerport'],
];

const urls = [
  ...topLevelPages.map((p) => `${base}${p}`),
  ...serviceSlugs.map((s) => `${base}/services/${s}/`),
  ...areaSlugs.map((a) => `${base}/locations/${a}/`),
  ...insightSlugs.map((i) => `${base}/insights/${i}/`),
  ...guideSlugs.map((g) => `${base}/guides/${g}/`),
  ...crossProductPages.map(([s, a]) => `${base}/services/${s}/${a}/`),
];

const xml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls.map((loc) => `  <url><loc>${loc}</loc><lastmod>${lastmod}</lastmod></url>`).join('\n')}
</urlset>
`;

const out = join(__dirname, '../dist/sitemap.xml');
writeFileSync(out, xml, 'utf8');
console.log('Wrote dist/sitemap.xml with', urls.length, 'URLs');
